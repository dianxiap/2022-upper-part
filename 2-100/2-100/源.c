#define _CRT_SECURE_NO_WARNINGS 1
#include <stdio.h>
int main()
{
	double i, n=0;
	scanf("%lf", &i);
	if (i <= 10)
	{
		n = i * 0.1;
	}
	else if (i > 10 && i <= 20)
	{
		n = (i - 10) * 0.075 + 10 * 0.1;
	}
	else if (i > 20 && i <= 40)
	{
		n = 10 * 0.1 + 10 * 0.075 + (i - 20) * 0.05;
	}
	else if (i > 40 && i <= 60)
	{
		n = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + (i - 40) * 0.03;
	}
	else if (i > 60 && i <= 100)
	{
		n = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + 20 * 0.03 + (i - 60) * 0.015;
	}
	else
		n = 10 * 0.1 + 10 * 0.075 + 20 * 0.05 + 20 * 0.03 + 40 * 0.015 + (i - 100) * 0.01;
	printf("%f", n);
}